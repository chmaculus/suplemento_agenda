<?php

function dia_semana($num){
	if($num==1){
		return "Lunes";
	}
	if($num==2){
		return "Martes";
	}
	if($num==3){
		return "Miercoles";
	}
	if($num==4){
		return "Jueves";
	}
	if($num==5){
		return "Viernes";
	}
	if($num==6){
		return "Sabado";
	}
	if($num==7){
		return "Domingo";
	}

}





?>